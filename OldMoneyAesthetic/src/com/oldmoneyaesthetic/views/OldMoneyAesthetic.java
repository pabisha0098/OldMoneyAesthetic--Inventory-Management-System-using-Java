    public void getAllTableData() {
        int row = OldMoneyAesthetic_table.getRowCount();

        for (int i = 0; i < row; i++) {
            OldMoneyAestheticModel model = new OldMoneyAestheticModel(
                    Integer.parseInt(
                            OldMoneyAesthetic_table.getValueAt(i, 0).toString()),
                    OldMoneyAesthetic_table.getValueAt(i, 1).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 2).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 3).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 4).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 5).toString(),
                    Integer.parseInt(
                    OldMoneyAesthetic_table.getValueAt(i, 6).toString()),
                    OldMoneyAesthetic_table.getValueAt(i, 7).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 8).toString(),
                    OldMoneyAesthetic_table.getValueAt(i, 9).toString()
            );
            clothingList.add(model);
        }
    }
    
    
    
