    private void jButtonSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSortActionPerformed

        
    String selectedColumn = (String) sortcolumn.getSelectedItem();
    ArrayList<OldMoneyAestheticModel> data = new ArrayList<>();
    DefaultTableModel tableModel = (DefaultTableModel) OldMoneyAesthetic_table.getModel();

    for (int i = 0; i < tableModel.getRowCount(); i++) {
    int sn = Integer.parseInt(tableModel.getValueAt(i, 0).toString());
    String clothing = tableModel.getValueAt(i, 1).toString();
    String brand = tableModel.getValueAt(i, 2).toString();
    String size = tableModel.getValueAt(i, 3).toString();
    String design = tableModel.getValueAt(i, 4).toString();
    String color = tableModel.getValueAt(i, 5).toString();
    int price = Integer.parseInt(tableModel.getValueAt(i, 6).toString());
    String designer = tableModel.getValueAt(i, 7).toString();
    String occasion = tableModel.getValueAt(i, 8).toString();
    String fabric = tableModel.getValueAt(i, 9).toString();
    
    OldMoneyAestheticModel item = new OldMoneyAestheticModel(sn, clothing, brand, size, design, color, price, designer, occasion, fabric);
    data.add(item);
    }
    
   
    MergeSortTable sorter = new MergeSortTable();
    sorter.sortArrayList(data, selectedColumn);
    
    tableModel.setRowCount(0);
    for (OldMoneyAestheticModel item : data) {
    tableModel.addRow(new Object[]{item.getSn(), item.getClothing(), item.getBrand(), item.getSize(), item.getDesign(), item.getColor(), item.getPrice(), item.getDesigner(), item.getOccasion(), item.getFabric()});

    }    
     JOptionPane.showMessageDialog(this, "Sorted successfully");
   
    }//GEN-LAST:event_jButtonSortActionPerformed

